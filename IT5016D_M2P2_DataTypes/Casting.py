# variable set to integer
my_score = 201

print("The type of my_score is ", type(my_score), "\n")  # type() prints out the type of variable
print("my_score is ", my_score, "\n")  # \n means new line

print("Casting to a string...\n")
my_score = str(my_score)  # casts the variable from integer to string

print("The type of my_score is now ", type(my_score), "\n")
print("my_score is ", my_score, "\n")

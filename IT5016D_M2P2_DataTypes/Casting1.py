# Casting, Learning Activity 20
my_size = 8.5

print("The type of my_size is ", type(my_size), "\n")
print("my_size is ", my_size, "\n")

# variable cast to float
print("Casting to a Float...\n")
my_size = float(my_size)  # casts the variable to float

print("The type of my_size is now ", type(my_size), "\n")
print("my_size is ", my_size, "\n")

# variable cast to integer
print("Casting to a Integer...\n")
my_size = int(my_size)  # casts the variable to integer

print("The type of my_size is now ", type(my_size), "\n")
print("my_size is ", my_size, "\n")